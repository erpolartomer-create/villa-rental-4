export function getVillaBySlug(slug: string): Villa | undefined {
  return villas.find(v => v.slug === slug)
}

export function getFeaturedVillas(): Villa[] {
  return villas.filter(v => v.isFeatured && v.status === 'active')
}

export function getDiscountedVillas(): Villa[] {
  return villas.filter(v => v.discountPercent && v.discountPercent > 0 && v.status === 'active')
}

export function getHoneymoonVillas(): Villa[] {
  return villas.filter(v => v.isHoneymoon && v.status === 'active')
}

export function getVillasByCategory(categorySlug: string): Villa[] {
  return villas.filter(v => v.categories.includes(categorySlug) && v.status === 'active')
}

export function searchVillas(filters: {
  checkin?: string
  checkout?: string
  guests?: number
  minPrice?: number
  maxPrice?: number
  bedrooms?: number
  categories?: string[]
  amenities?: string[]
}): Villa[] {
  return villas.filter(v => {
    if (v.status !== 'active') return false
    if (filters.guests && v.maxGuests < filters.guests) return false
    if (filters.minPrice && v.basePricePerNight < filters.minPrice) return false
    if (filters.maxPrice && v.basePricePerNight > filters.maxPrice) return false
    if (filters.bedrooms && v.bedrooms < filters.bedrooms) return false
    if (filters.categories && filters.categories.length > 0) {
      if (!filters.categories.some(c => v.categories.includes(c))) return false
    }
    if (filters.amenities && filters.amenities.length > 0) {
      if (!filters.amenities.every(a => v.amenities.includes(a))) return false
    }
    return true
  })
}

